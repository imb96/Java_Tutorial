package variable;

public class Var8 {
    public static void main(String[] args) {
        // 정수
        byte b = 127; // -128 ~ 127
        short s = 32767; // -32,768 ~ 32,768
        int i = 2147483647; // -2147483647 ~ 2147483647
        long l = 9223372036854775807L; // -9,223,372,036,854,775,808 ~ 9,223,372,036,854,775,808

        // 실수
        float f = 10.0f;
        double d = 10.0;
    }
    // 기본적으로 정수는 int, 실수는 double 사용, 문자열은 String
}
